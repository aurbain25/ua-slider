class Options {
    slidesToScroll: number // Number of elements to scroll
    slidesVisible: number // Number of elements visible in a slider
    navigation: boolean // Active the navigation
    navigationThumbnail: boolean // Active the navigation by Thumbnail
    pagination: boolean // Active the pagination
    infinite: boolean // Active infinite slider
    autoplay: boolean // Active autoplay slider
    autoplaySpeed: number // Speed autoplay slider
    slideSpace: number // Space between two slide
    slideFix: number // Location of the fix slide (first or last slide visible)
    touchActive: boolean // Active touch slider
}